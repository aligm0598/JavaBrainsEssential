public class ifelse {

    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        if (a > b) {
            System.out.println("A is greater");
        } else if (a < b) {
            System.out.println("A is lesser");
        } else {
            System.out.println("They are equal");
        }
    }
}
